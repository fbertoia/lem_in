#include "libft.h"

void	del_arr(char ***arr)
{
	int i;

	i = 0;
	if (!*arr)
		return ;
	while ((*arr)[i])
	{
		ft_memdel((void**)&(*arr)[i]);
		i++;
	}
	ft_memdel((void**)&(*arr));
}
